package BitManipulation;

import java.util.*;

public class bitwise_operator {
    public static void main(String[] args) {

        // Bitwise AND &
        System.out.println(5 & 6);

        // Bitwise OR |
        System.out.println(5 | 6);

        // Bitwise XOR ^
        System.out.println(5 ^ 6);

        // Binary Ones's Complemet ~
        System.out.println(~0);

        // Binary Left shift <<
        System.out.println(5 << 2);

        // Binary Right Shift >>
        System.out.println(6 >> 1);
    }
}